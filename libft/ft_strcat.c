#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*ss2;
	size_t	i;
	size_t	j;

	ss2 = (char *)s2;
	i = 0;
	while (s1[i] != '\0')
		i++;
	j = 0;
	while (ss2[j] != '\0')
	{
		s1[i] = ss2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
